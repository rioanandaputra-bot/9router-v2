/**
 * Get rich parameter definitions for an image (or video) model.
 *
 * Returns an array of param schema objects — each containing:
 *   { name, type, required, default, validValues?, min?, max?, description }
 *
 * Falls back to an empty array when:
 *   - the provider/model is not found
 *   - the model is not of type "image" or "video"
 *   - no paramDefs were defined for this model
 *
 * @param {string} aliasOrId - provider alias or id (e.g. "openai", "weavy")
 * @param {string} modelId   - model id (e.g. "dall-e-3", "weavy-flux-pro")
 * @returns {object[]}
 */
export function getModelImageParams(aliasOrId, modelId) {
  const alias = PROVIDER_ID_TO_ALIAS[aliasOrId] || aliasOrId;
  const models = PROVIDER_MODELS[alias];
  if (!models) return [];
  const entry = models.find(m => m.id === modelId);
  if (!entry) return [];
  if (entry.type !== "image" && entry.type !== "video") return [];
  return entry.paramDefs || [];
}

/**
 * Get the human-readable description for any model entry.
 *
 * @param {string} aliasOrId - provider alias or id
 * @param {string} modelId   - model id
 * @returns {string|null}
 */
